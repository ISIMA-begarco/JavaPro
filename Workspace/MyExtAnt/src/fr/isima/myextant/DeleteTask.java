package fr.isima.myextant;

public class DeleteTask extends Task {
	
	protected String attr[];
	
	public DeleteTask() {
		attr = new String[1];
	}
	
	public DeleteTask(String a) {
		attr = new String[1];
		attr[0] = a;
	}
	
	public void setAtt(String s) {
		attr[0] = s;
	}
	
	public String getAtt() {
		return attr[0];
	}
	
	@Override
	public void execute() {
		System.out.println("Supression de "+attr[0]);
	}
}
