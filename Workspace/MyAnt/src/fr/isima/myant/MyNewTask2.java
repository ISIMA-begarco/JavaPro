package fr.isima.myant;

public class MyNewTask2 extends Task {
	
	protected String attr[];
	
	public MyNewTask2() {
		attr = new String[3];
	}
	
	public MyNewTask2(String a, String b, String c) {
		attr = new String[3];
		attr[0] = a;
		attr[1] = b;
		attr[2] = c;
	}
	
	public void setAtt1(String s) {
		attr[0] = s;
	}
	
	public void setAtt2(String s) {
		attr[1] = s;
	}
	
	public void setAtt3(String s) {
		attr[2] = s;
	}
	
	public String getAtt1() {
		return attr[0];
	}
	
	public String getAtt2() {
		return attr[1];
	}
	
	public String getAtt3() {
		return attr[2];
	}
	
	@Override
	public void execute() {
		System.out.println(attr[0]+" "+attr[1]+" "+attr[2]);
	}

}
